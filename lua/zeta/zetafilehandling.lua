    function RegisterMaterial(ply,cmd,args) -- Used to add a material to the Zeta's Valid Materials
        if !ply:IsSuperAdmin() then ply:PrintMessage(HUD_PRINTCONSOLE,'Only Super Admins are allowed to register materials!') return end
        if !args[1] then ply:PrintMessage(HUD_PRINTCONSOLE,"Provided Material either does not exist or is NIL") return end
        local _string = args[1]

        local mat,num = Material(_string)

        if mat and !mat:IsError() then
            local json = file.Read("zetaplayerdata/materials.json","DATA")
            local decoded = util.JSONToTable(json)
            if table.HasValue(decoded,_string) then ply:PrintMessage(HUD_PRINTCONSOLE,_string.." is already registered!") return end
            table.insert(decoded,_string)
            local encoded = util.TableToJSON(decoded)
            file.Write("zetaplayerdata/materials.json",encoded)
            ply:PrintMessage(HUD_PRINTCONSOLE,"Added ".._string.." To the Zeta's Valid Materials")
            hook.Run("OnUpdateRegisteredMats",_string)
        else
            ply:PrintMessage(HUD_PRINTCONSOLE,"Provided Material either does not exist or is NIL")
        end

    end

    function RemoveMaterial(ply,cmd,args) -- Remove a Registered Material
        if !ply:IsSuperAdmin() then ply:PrintMessage(HUD_PRINTCONSOLE,'Only Super Admins are allowed to remove materials!') return end
        if !args[1] then ply:PrintMessage(HUD_PRINTCONSOLE,"Provided Material either does not exist or is NIL") return end
